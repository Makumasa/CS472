#include <math.h>

double add_d(double op1, double op2) {
    return op1 + op2;
}

double sub_d(double op1, double op2) {
    return op1 - op2;
}

double mul_d(double op1, double op2) {
    return op1 * op2;
}

double div_d(double op1, double op2) {
    return op1 / op2;
}

double sqrt_d(double op) {
    return sqrt(op);
}
